tuoi_tac(GV, Tuoi) :-
	nam_sinh(GV,Year),
	Tuoi is 2019 - Year.

tuoi_bang_nhau(X,Y) :-
	tuoi_tac(X, Tuoi),
	tuoi_tac(Y, Tuoi),
	X \= Y.

tuoi_lon_hon(X, Y) :-
	tuoi_tac(X, Tuoi1),
	tuoi_tac(Y, Tuoi2),
	X \= Y,
	Tuoi1 > Tuoi2.

tuoi_nho_hon(X, Y) :-
	tuoi_tac(X, Tuoi1),
	tuoi_tac(Y, Tuoi2),
	X \= Y,
	Tuoi1 < Tuoi2.
 
